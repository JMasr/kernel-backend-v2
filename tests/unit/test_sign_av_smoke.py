async def test_sign_av_uses_smaller_rs_n(synthetic_av: Path) -> None:
    """sign_av uses min(rs_n_audio, rs_n_video) as the shared codeword length."""
    private_pem, public_pem = generate_keypair()
    cert = _make_cert(public_pem)
    registry = FakeRegistry()

    result = await sign_av(
        media_path=synthetic_av,
        certificate=cert,
        private_key_pem=private_pem,
        storage=FakeStorage(),
        registry=registry,
        pepper=PEPPER,
        media=MediaService(),
    )

    # 120s video → video: 120/5 = 24 segments, audio: 120/2 = 60 segments
    # rs_n_video=24 < rs_n_audio=60 → rs_n=24
    entry = registry._videos[result.content_id]
    assert entry.rs_n == result.rs_n
    assert result.rs_n <= 24  # video segments cap it


async def test_shared_wid_is_identical_for_both_channels(synthetic_av: Path) -> None:
    """
    The same WID is embedded in both audio and video tracks.
    Verified by checking that re-deriving WID from the stored signature
    matches the returned WID — one signature → one WID for both channels.
    """
    private_pem, public_pem = generate_keypair()
    cert = _make_cert(public_pem)
    registry = FakeRegistry()

    result = await sign_av(
        media_path=synthetic_av,
        certificate=cert,
        private_key_pem=private_pem,
        storage=FakeStorage(),
        registry=registry,
        pepper=PEPPER,
        media=MediaService(),
    )

    # Re-derive WID from the stored signature
    entry = registry._videos[result.content_id]
    rederived_wid = derive_wid(entry.manifest_signature, result.content_id)

    # Single WID — same object for both channels
    assert result.wid.data == rederived_wid.data
    # There should be only ONE VideoEntry (not two separate audio/video entries)
    assert len(registry._videos) == 1
