    """120-second AV file (testsrc video + sine audio). ≥17 segments for both tracks."""
    tmp = tmp_path_factory.mktemp("av")
    out = tmp / "test_av.mp4"
    subprocess.run(
        [
            "ffmpeg", "-y",
            "-f", "lavfi", "-i", "testsrc=duration=120:size=320x240:rate=25",
            "-f", "lavfi", "-i", "sine=frequency=440:duration=120",
            "-c:v", "libx264", "-crf", "28", "-preset", "ultrafast",
            "-c:a", "aac", "-ar", "44100",
            str(out),
        ],
        check=True,
        capture_output=True,
    )
    return out


