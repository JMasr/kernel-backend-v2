from kernel_backend.core.services.verification_service import VerificationService
from kernel_backend.infrastructure.media.media_service import MediaService
from tests.helpers.fakes import FakeRegistry, FakeStorage

PEPPER = b"integration-test-pepper-padded!!"


def _make_cert(public_key_pem: str) -> Certificate:
    return Certificate(
        author_id="integration-author",
        name="Integration Test Author",
        institution="Test Org",
        public_key_pem=public_key_pem,
        created_at="2026-01-01T00:00:00+00:00",
    )


@pytest.fixture(scope="module")
def synthetic_av_120s(tmp_path_factory) -> Path:
    """120-second AV file — ≥17 segments for both audio (60 segs) and video (24 segs).

    320x240 lossless (CRF 0) source chosen deliberately:
    - CRF 0 eliminates double-quantization when test recompresses at CRF 23/28.
    - 320x240 gives 4800 blocks/frame — enough margin for QIM_STEP=48 at CRF 28.
      (160x120 = 1200 blocks was insufficient for CRF 28 survival.)
    - 15 fps keeps file size manageable (~70 MB CRF 0 lossless).
    """
    tmp = tmp_path_factory.mktemp("integ_av")
    out = tmp / "av_120s.mp4"
    subprocess.run(
        [
            "ffmpeg", "-y",
            "-f", "lavfi", "-i", "testsrc=duration=120:size=320x240:rate=15,noise=c0s=100:allf=t",
            "-f", "lavfi", "-i", "anoisesrc=duration=120:sample_rate=44100",
            "-ac", "1",
            "-c:v", "libx264", "-crf", "0", "-preset", "ultrafast",
            "-c:a", "aac", "-ar", "44100",
            str(out),
        ],
        check=True,
        capture_output=True,
    )
    return out


