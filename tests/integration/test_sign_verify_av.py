"""
Integration tests for sign_av → verify_av end-to-end pipeline.

These tests use synthetic AV files generated by ffmpeg and are marked
@pytest.mark.slow. They are excluded from default CI runs.

Run:
    pytest tests/integration/test_sign_verify_av.py -v
"""
from __future__ import annotations

import subprocess
import tempfile
from pathlib import Path

import pytest

from kernel_backend.core.domain.identity import Certificate
from kernel_backend.core.domain.verification import AVVerificationResult, RedReason, Verdict
from kernel_backend.core.services.crypto_service import generate_keypair
